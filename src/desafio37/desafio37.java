package desafio37;

public class desafio37 {

public static void main(String[] args) {
	
	for (char letra = 'A'; letra <= 'Z'; letra ++) {
		
		
		
		if (letra == 'A' || letra == 'E' || letra == 'I' || letra == 'O' || letra == 'U') {
			
			System.out.println("Encontre una vocal: " + letra);
		}
		else {
			
			System.out.println(letra);
		}
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
}	
}
